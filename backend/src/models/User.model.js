const { Schema, model } = require("mongoose");

const UserSchemea = new Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true },
);

const UserModel = model("users", UserSchemea);

module.exports = UserModel;
